const array1 = ['a', 'b', 'c'];
const array2 = ['d', 'e', 'f'];
const array3 = array1.concat(array2);

console.log(array3);
// expected output: Array ["a", "b", "c", "d", "e", "f"]

// Metoden "concat" används för att slå samman två eller flera arrayer. 
// Denna metod ändrar inte de befintliga arrayerna, 
// utan returnerar istället en ny array.